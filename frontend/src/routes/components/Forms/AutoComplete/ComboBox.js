export const ComboBox = (props) => {
	let text = '';
	let dispList = [];
	if (props.displayList == 'Country') {
		text = 'Select Country/Team';
		dispList = countryList;
	} else if (props.displayList == 'StartYear') {
		text = 'Select Start Year';
		dispList = yearList;
	} else if (props.displayList == 'Year') {
		text = 'Select Year';
		dispList = yearList;
	} else if (props.displayList == 'EndYear') {
		text = 'Select End Year';
		dispList = yearList;
	} else if (props.displayList == 'Player') {
		text = 'Select Player';
		if (playerList.hasOwnProperty(props.ccountry)) {
			dispList = playerList[props.ccountry];
		} else {
			dispList = [];
		}
	} else if (props.displayList == 'Tournament Stats') {
		text = 'Select Stats';
		dispList = Object.keys(statsListTournament);
	} else if (props.displayList == 'Team Stats') {
		text = 'Select Stats';
		dispList = Object.keys(statsListTeam);
	}
	return (
		<BaseCard title={text}>
			<Autocomplete
				disablePortal
				id='combo-box-demo'
				options={dispList}
				fullWidth
				onChange={(e, newValue) => {
					props.onChange(newValue);
				}}
				renderInput={(params) => <TextField {...params} label='Combo Box' />}
			/>
		</BaseCard>
	);
};

